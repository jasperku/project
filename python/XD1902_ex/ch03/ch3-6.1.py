#遞迴函式名稱
def fact(N):  
   if (N ==1):
     return 1
   else:
     return N * fact(N - 1)#函式自己又可以呼叫自己
Max = 10
Sum = fact(Max)             #呼叫遞迴函式
print("10!=1*2*...*10=%3d" %Sum) 
