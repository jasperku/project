#遞迴函式名稱
def GCD(a,b):  
    c = a % b;
    if (c == 0):
        return b
    else:
        return GCD(b, c)  #函式自己又可以呼叫自己
a,b=18,15
print("a=18,b=15之")
print("最大公因數為=%2d" %GCD(a, b))
